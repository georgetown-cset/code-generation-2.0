#include <stdio.h> /*This is the preprocessor directive which lets us use the print function defined in this module*/
#include <stdlib.h> /*This is another preprocessor directive which includes the contents of the standard library in our program.*/

int main() {
    printf("Hello, World!"); /*Print function from stdio module */
    return 0; /*As per standard, the main function should return an integer type value at the end which is why we are returning 0*/
}
